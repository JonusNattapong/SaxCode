import chalk from 'chalk';
import ora from 'ora';

const CLAUDE_ORANGE = '#da7756';
const orange = chalk.hex(CLAUDE_ORANGE);

export const logger = {
  info: (msg: string) => console.log(`${chalk.blue('‚ĄĻ')} ${msg}`),
  success: (msg: string) => console.log(`${chalk.green('‚úĒ')} ${msg}`),
  warn: (msg: string) => console.log(`${chalk.yellow('‚ö†')} ${msg}`),
  error: (msg: string) => console.log(`${chalk.red('‚úĖ')} ${msg}`),
  agent: (msg: string) => console.log(`${orange('‚óą')} ${chalk.bold(msg)}`),
  tool: (name: string, args: any) => {
    console.log(`${chalk.cyan('‚öí')} Executing ${chalk.cyan.bold(name)}...`);
    if (args) {
      console.log(chalk.gray(`  ${JSON.stringify(args)}`));
    }
  },
  spinner: (msg: string) => ora({
    text: msg,
    color: 'yellow', // ora doesn't support custom hex, yellow is closest to orange
    spinner: 'dots'
  }).start()
};

